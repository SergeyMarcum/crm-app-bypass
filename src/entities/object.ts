// Основной интерфейс для объекта
export interface Object {
  id: string | number; // Уникальный идентификатор объекта (в JSON строка, в API может быть число)
  name: string; // Название объекта (например, "Станция подстанция №3")
  address: string; // Адрес объекта (например, "г. Соль-Илецк")
  domain: string; // Домен объекта (например, "orenburg" или "irkutsk")
  parameters?: number[]; // Массив ID параметров, связанных с объектом (опционально)
  feature?: string; // Описание характеристик объекта (опционально, из JSON)
}

// Расширенный интерфейс для объекта с дополнительными данными (если потребуется)
export interface ExtendedObject extends Object {
  created_at?: string; // Дата создания объекта (опционально)
  updated_at?: string; // Дата последнего обновления (опционально)
  status?: string; // Статус объекта (например, "активен", "на проверке")
}
